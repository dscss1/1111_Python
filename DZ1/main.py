import turtle

def main():
    turtle.shape("turtle")
    turtle.pensize(10)
    turtle.forward(100)
    turtle.left(90)
    turtle.forward(100)
    turtle.penup()
    turtle.color("red", "yellow")
    turtle.pendown()
    turtle.left(90)
    turtle.forward(100)
    turtle.left(180)
    turtle.forward(150)
    turtle.done()
